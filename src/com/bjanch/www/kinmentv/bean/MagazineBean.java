package com.bjanch.www.kinmentv.bean;

public class MagazineBean {

	private String pubName;		//名称
	private String pubUrl;		//杂志网址
	private String pubCoverUrl;	//图片地址

	public String getPubName() {
		return pubName;
	}

	public void setPubName(String pubName) {
		this.pubName = pubName;
	}

	public String getPubUrl() {
		return pubUrl;
	}

	public void setPubUrl(String pubUrl) {
		this.pubUrl = pubUrl;
	}

	public String getPubCoverUrl() {
		return pubCoverUrl;
	}

	public void setPubCoverUrl(String pubCoverUrl) {
		this.pubCoverUrl = pubCoverUrl;
	}
}
